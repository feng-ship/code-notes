#include <stdio.h>

int main() {
    int a[5] = {3, 9, 5, 7, 4};

    // 查找第一个偶数
    for (int i = 0; i < 5; i++) {
        if (a[i] % 2 == 0) {
            printf("找到偶数：%d\n", a[i]);
            return 0; // 在循环里直接结束程序
        }
    }

    // 如果循环结束了还没 return，说明没有偶数
    printf("没有找到偶数\n");

    return 0;
}

/*
输出结果：
找到偶数：4

理解：
1. return 0 在循环里出现时，程序会立刻结束
2. 如果循环没提前结束，就会执行循环外的语句
3. 这种写法适合“多种情况选择”的题目
*/